public class AbbreviatingOperators {
    public static void main(String[] args) {
        // % - remainder operator
        int result = 0;

        result++; // 0 + 1 = 1
        System.out.println(result); // 1

        result--;
        System.out.println(result); // 0

        result += 2; // 0 + 2 = 2
        System.out.println(result);

    }
}
